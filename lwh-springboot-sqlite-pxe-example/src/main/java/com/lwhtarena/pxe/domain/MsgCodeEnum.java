public enum MsgCodeEnum {

    sucess(0000,"成功"),
    fail(1000,"失败"),
    msg1001(1001,"网卡信息不能为空"),


    ;

    Integer code;
    String value;

    MsgCodeEnum(Integer code, String value) {
        this.code = code;
        this.value = value;
    }

    public Integer getCode() {
        return code;
    }

    public void setCode(Integer code) {
        this.code = code;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
